package mypackage;

public class Encapsulation {
  private int age;
  private boolean decision;

  public int getAge() {
    return age;
  }
  public void setAge(int age) {
    this.age = age;
  }
  public boolean isDecision() {
    return decision;
  }
  public void setDecision(boolean decision) {
    this.decision = decision;
  }
}
